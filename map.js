function loadImage(src) {
  const img = new Image();
  img.src = src;
  return img;
}

function addImageAndDraw(x, y, index) {
  const img = loadImage(`assets/ground/${index}.png`);
  images.push(img);

  img.onload = function() {
    mapCtx.drawImage(img, x, y, tileSize, tileSize);
  };
}

function drawMap() {
  for (let i = 0; i < mapIndex.length; i++) {
    for (let j = 0; j < mapIndex[i]. length; j++) {
      let index = mapIndex[i][j];
      if (index != 0) {
        const x = j * tileSize;
        const y = i * tileSize;
        addImageAndDraw(x, y, index);
      }
    }
  }
}

drawMap();
